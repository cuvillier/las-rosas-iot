	THG_DISCRIMINATOR VARCHAR(3) NOT NULL,
	THG_FK_TTY_TYPE INT UNSIGNED NOT NULL,
	THG_FK_DTW_TWIN INT UNSIGNED DEFAULT NULL,
	THG_FK_GTW_GATEWAY INT UNSIGNED,
	THG_LDTY_VISIBLE datetime DEFAULT NULL,
	THG_MODE VARCHAR(10) DEFAULT 'enabled' NOT NULL,
	BCO_EDDYSTONE_UID VARCHAR(50) DEFAULT NULL,
	BCO_IBEACON_UUID VARCHAR(50) DEFAULT NULL,
	BCO_IBEACON_MAJOR VARCHAR(50) DEFAULT NULL,
	BCO_IBEACON_MINOR VARCHAR(50) DEFAULT NULL,
	LOR_DEV_EUI VARCHAR(50) DEFAULT NULL,
	LOR_APP_EUI VARCHAR(50) DEFAULT NULL,
	LOR_APP_KEY VARCHAR(50) DEFAULT NULL,
	WIF_BSSID VARCHAR(50) DEFAULT NULL,
	WIF_SSID VARCHAR(50) DEFAULT NULL,
	MOB_IMEI VARCHAR(30) DEFAULT NULL,
	TST_SERIAL VARCHAR(30) DEFAULT NULL,
	WST_STATION_ID VARCHAR(50) DEFAULT NULL,
	PRIMARY KEY (THG_TECHID),
	CONSTRAINT FK_THG_FK_TTY_TYPE FOREIGN KEY (THG_FK_TTY_TYPE) REFERENCES T_THG_THING_TYPE(TTY_TECHID),
	CONSTRAINT FK_THG_FK_DTW_TWIN FOREIGN KEY (THG_FK_DTW_TWIN) REFERENCES T_DTW_DIGITAL_TWIN (DTW_TECHID),
	CONSTRAINT FK_THG_FK_GTW_GATEWAY FOREIGN KEY (THG_FK_GTW_GATEWAY) REFERENCES T_THG_GATEWAY (GTW_TECHID),
	CONSTRAINT FK_THG_MODE CHECK(THG_MODE IN ('Enabled', 'Disabled', 'Suspended'))
