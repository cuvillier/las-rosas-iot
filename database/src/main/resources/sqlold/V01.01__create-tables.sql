/*------------------------------------------- DIGITAL_TWIN -------------------------------------------*/

CREATE TABLE T_DTW_SPACE
(
	SPA_TECHID INT UNSIGNED AUTO_INCREMENT,
	SPA_READABLE VARCHAR(50) DEFAULT NULL,

	PRIMARY KEY (SPA_TECHID)
);

CREATE TABLE T_DTW_DIGITAL_TWIN_TYPE
(
	DTY_TECHID INT UNSIGNED AUTO_INCREMENT,
	DTY_READABLE VARCHAR(50) DEFAULT NULL,
	DTY_DISCRIMINATOR VARCHAR(3) NOT NULL,

	DTY_REFID VARCHAR(50) NOT NULL,
	DTY_LOCALIZABLE VARCHAR(10) DEFAULT 'No',
	DTY_FK_SPA_SPACE INT UNSIGNED NOT NULL,
	YTY_FK_DTY_SUPER_TYPE INT UNSIGNED DEFAULT NULL,
	YTY_CONCRETE BIT DEFAULT 1,

	PRIMARY KEY (DTY_TECHID),
	CONSTRAINT FK_YTY_FK_DTY_SUPER_TYPE FOREIGN KEY (YTY_FK_DTY_SUPER_TYPE) REFERENCES T_DTW_DIGITAL_TWIN_TYPE (DTY_TECHID),
	CONSTRAINT FK_DTY_FK_SPA_SPACE FOREIGN KEY (DTY_FK_SPA_SPACE) REFERENCES T_DTW_SPACE (SPA_TECHID)
);


CREATE TABLE T_DTW_DYNAMIC_TWIN_LINK_TYPE
(
	DLT_TECHID INT UNSIGNED AUTO_INCREMENT,

	DLT_FK_DTY_ORIGIN INT UNSIGNED NOT NULL,
	DLT_FK_DTY_DESTINATION INT UNSIGNED NOT NULL,
	DLT_CARD_MIN INTEGER DEFAULT 0,
	DLT_CARD_MAX INTEGER NULL,

	PRIMARY KEY (DLT_TECHID),
	CONSTRAINT FK_DLT_FK_DTY_ORIGIN FOREIGN KEY (DLT_FK_DTY_ORIGIN) REFERENCES T_DTW_DIGITAL_TWIN_TYPE (DTY_TECHID),
	CONSTRAINT FK_DLT_FK_DTY_DESTINATION FOREIGN KEY (DLT_FK_DTY_DESTINATION) REFERENCES T_DTW_DIGITAL_TWIN_TYPE(DTY_TECHID)
);

CREATE TABLE T_DTW_DYNAMIC_TWIN_PROPERTY_TYPE
(
	DPT_TECHID INT UNSIGNED AUTO_INCREMENT,
	DPT_READABLE VARCHAR(50) DEFAULT NULL,

	DPT_FK_DTY_TWIN_TYPE INT UNSIGNED NOT NULL,
	DPT_FORMAT VARCHAR(32) NOT NULL,
    PRIMARY KEY  (DPT_TECHID),
	CONSTRAINT FK_DPT_FK_DTY_TWIN_TYPE FOREIGN KEY (DPT_FK_DTY_TWIN_TYPE) REFERENCES T_DTW_DIGITAL_TWIN_TYPE(DTY_TECHID)
);



CREATE TABLE T_DTW_DIGITAL_TWIN
(
	DTW_TECHID INT UNSIGNED AUTO_INCREMENT,
	DTW_DISCRIMINATOR VARCHAR(3) NOT NULL,
	DTW_PATHID VARCHAR(200),
	DTW_READABLE VARCHAR(50),
	DTW_FK_DTY_TYPE INT UNSIGNED NOT NULL,
	DTW_FK_DTW_PARTOF INT UNSIGNED,
	DTW_LONGITUDE Decimal(9,6) DEFAULT NULL,
	DTW_LATITUDE Decimal(9,6) DEFAULT NULL,
	DTW_LOCALIZATION_TIME datetime DEFAULT NULL,
	PRIMARY KEY (DTW_TECHID),
	CONSTRAINT FK_DTW_FK_DTW_PARTOF FOREIGN KEY (DTW_FK_DTW_PARTOF) REFERENCES T_DTW_DIGITAL_TWIN (DTW_TECHID),
	CONSTRAINT FK_DTW_FK_DTY_TYPE FOREIGN KEY (DTW_FK_DTY_TYPE) REFERENCES T_DTW_DIGITAL_TWIN_TYPE(DTY_TECHID)
);

CREATE TABLE T_DTW_DYNAMIC_TWIN_PROPERTY
(
	DPR_TECHID INT UNSIGNED AUTO_INCREMENT,
	DPR_FK_TEN_TENANT BIGINT NULL ,

	DPR_FK_DPT_TYPE INT UNSIGNED NOT NULL,
	DPR_FK_DTW_TWIN INT UNSIGNED NOT NULL,
	DPR_VALUE VARCHAR(4000) NOT NULL, 
    PRIMARY KEY (DPR_TECHID),
	CONSTRAINT FK_DPR_FK_DPT_TYPE FOREIGN KEY (DPR_FK_DPT_TYPE) REFERENCES T_DTW_DYNAMIC_TWIN_PROPERTY_TYPE(DPT_TECHID),
	CONSTRAINT FK_DPR_FK_DTW_TWIN FOREIGN KEY (DPR_FK_DTW_TWIN) REFERENCES T_DTW_DIGITAL_TWIN(DTW_TECHID)
);

