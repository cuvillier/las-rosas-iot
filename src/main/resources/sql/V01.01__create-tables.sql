CREATE UNIQUE NONCLUSTERED INDEX I_TST_SERIAL ON T_THG_THING(TST_SERIAL) WHERE TST_SERIAL IS NOT NULL
CREATE UNIQUE NONCLUSTERED INDEX I_BCO_IBEACON_UUID ON T_THG_THING(BCO_IBEACON_UUID,BCO_IBEACON_MAJOR,BCO_IBEACON_MINOR) WHERE BCO_IBEACON_UUID IS NOT NULL
CREATE UNIQUE NONCLUSTERED INDEX I_BCO_EDDYSTONE_UID ON T_THG_THING(BCO_EDDYSTONE_UID) WHERE BCO_EDDYSTONE_UID IS NOT NULL
CREATE UNIQUE NONCLUSTERED INDEX I_WIF_BSSID ON T_THG_THING(WIF_BSSID) WHERE WIF_BSSID IS NOT NULL

