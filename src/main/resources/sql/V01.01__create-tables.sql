CREATE TABLE T_THG_THING
(
	THG_TECHID BIGINT IDENTITY(10000,1) NOT NULL,
	THG_READABLE VARCHAR(50) DEFAULT NULL,
	THG_BEGTIME datetime2(7) DEFAULT SYSDATETIME ( ),
	THG_ENDTIME datetime2(7) DEFAULT NULL,
	THG_FK_TEN_TENANT BIGINT NULL ,
	THG_DISCRIMINATOR VARCHAR(3) NOT NULL,
	THG_FK_THM_MODEL BIGINT NOT NULL,
	THG_FK_ASS_ASSET BIGINT DEFAULT NULL,
	THG_LAST_VISIBLE datetime2(7) DEFAULT NULL,
	THG_MODE VARCHAR(10) DEFAULT 'enabled' NOT NULL,
	BCO_EDDYSTONE_UID VARCHAR(50) DEFAULT NULL,
	BCO_IBEACON_UUID VARCHAR(50) DEFAULT NULL,
	BCO_IBEACON_MAJOR VARCHAR(50) DEFAULT NULL,
	BCO_IBEACON_MINOR VARCHAR(50) DEFAULT NULL,
	LOR_DEV_EUI VARCHAR(50) DEFAULT NULL,
	LOR_APP_EUI VARCHAR(50) DEFAULT NULL,
	LOR_APP_KEY VARCHAR(50) DEFAULT NULL,
	WIF_BSSID VARCHAR(50) DEFAULT NULL,
	WIF_SSID VARCHAR(50) DEFAULT NULL,
	MOB_IMEI VARCHAR(30) DEFAULT NULL,
	TST_SERIAL VARCHAR(30) DEFAULT NULL,
	WST_STATION_ID VARCHAR(50) DEFAULT NULL,
	THG_ROUTE_SIDIM BIT DEFAULT 0,
	CONSTRAINT PK_THG_THING PRIMARY KEY CLUSTERED (THG_TECHID),
	CONSTRAINT FK_THG_FK_TEN_TENANT FOREIGN KEY (THG_FK_TEN_TENANT) REFERENCES T_TENANT (TEN_TECHID),
